package tree;

/*
 * 
 * 基于链表的二叉树的树结点
 * 
 */
public class linkListBinaryTreeNode<T> { 
	
	
		 T data  ;  //结点的数据
		 linkListBinaryTreeNode<T> left ;  //左子结点
		 linkListBinaryTreeNode<T> right ; //右子结点
		
		
		//用于创建结点的构造函数
		public linkListBinaryTreeNode(T data){
			
			this.data = data ;
			this.left = null ;
			this.right = null ;
		
		}
	
	
	
	
	
	
	
	
	
	
	

}
